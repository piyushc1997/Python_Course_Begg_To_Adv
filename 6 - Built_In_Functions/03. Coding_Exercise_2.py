"""

Calculate Power of a Number

You are required to write a Python program that calculates and prints the result of 2 raised to the power 8.

The pow() function in Python accepts two arguments and returns the first argument raised to the power of the second argument. 

Your task is to use the pow() function to calculate 2 to the power of 8 and print the result.

Input Format :
There are no inputs for this task.

Output Format :
The output should be a single integer, the result of 2 raised to the power 8 is 256.


"""

# Solution

base = 2
exponent = 8

result = pow(base, exponent)

print(result)
