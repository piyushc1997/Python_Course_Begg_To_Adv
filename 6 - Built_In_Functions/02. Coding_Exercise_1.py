"""

Calculate Absolute Value

Problem Statement :

In this exercise, your task is to write a Python program that calculates and prints the absolute value of -100.

The absolute value of a number is its distance from 0 on the number line, regardless of direction. 

In other words, the absolute value of -100 is 100 and the absolute value of 100 is also 100.

Input Format :
There are no inputs for this task.

Output Format :
The output should be a single integer, the absolute value of -100 is 100.


"""

# Solution

number = -100

absolute_value = abs(number)

print(absolute_value)
