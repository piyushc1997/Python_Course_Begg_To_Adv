"""

Finding the Maximum Number

Problem Statement

Your task is to write a Python program that determines and prints the maximum number among 3, 7, and 5.

Input Format :
There are no inputs for this task.

Output Format :
The output should be a single integer, which is the maximum among 3, 7, and 5.

"""

# Solution

numbers = [3, 7, 5]

max_number = max(numbers)

print(max_number)
